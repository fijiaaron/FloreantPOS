package scipio.model;

public class Coupon {
	private String name;
	private String number;
	
	///////////////////////////////////
	// Getters and Setters
	///////////////////////////////////
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getNumber() {
		return number;
	}
	public void setNumber(String number) {
		this.number = number;
	}
}
