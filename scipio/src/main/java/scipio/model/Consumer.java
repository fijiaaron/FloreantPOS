package scipio.model;

public class Consumer {
	private int pei;
	private int tei;
	private int pin;
	private String name;
	
	///////////////////////////////////
	// Getters and Setters
	///////////////////////////////////
	
	public int getPei() {
		return pei;
	}
	public void setPei(int pei) {
		this.pei = pei;
	}
	public int getTei() {
		return tei;
	}
	public void setTei(int tei) {
		this.tei = tei;
	}
	public int getPin() {
		return pin;
	}
	public void setPin(int pin) {
		this.pin = pin;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
	
}
